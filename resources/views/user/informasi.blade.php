            <p class="subtext font-medium"> <br>1. Pot bunga dari botol bekas </p>
            <p> Botol plastik bekas dapat dimanfaatkan kembali sebagai pot bunga lucu bergambar hewan. 
                Bahan-bahan yang dibutuhkan antara lain botol 2 liter dan pewarna atau cat. 
                Ikuti langkah-langkah berikut untuk membuatnya :
            
            <ol class ="indent-8">
                <li>1. Potong sepertiga bagian bawah botol</li>
                <li>2. Pada salah satu sisi, potong botol menjadi dua bentuk segitiga sehingga terlihat seperti telinga</li>
                <li>3. Cat botol dengan warna yang diinginkan</li>
                <li>4. Gambar wajah hewan pada botol, misalnya wajah kucing atau anjing</li>
                <li>5. Isi botol dengan tanah dan tanaman yang ingin ditanam</li>
            </ol>

            </p>

            <p class="subtext font-medium"> <br>2. Tempat pensil dari botol bekas </p>
            <p> Botol plastik juga bisa didaur ulang menjadi tempat pensil. 
                Caranya yakni dengan memotong botol menjadi setengah bagian, kemudian hiaslah dengan cara diwarnai dan diberi glitter. 
                Setelah selesai dihias, 
                masukkan pensil atau bolpoin ke dalam potongan botol tersebut.

            </p>

            <p class="subtext font-medium"> <br>3. Seni mozaik dari tutup botol</p>
            <p> Ketika seseorang membuat kerajinan dari botol plastik bekas, bagian tutupnya jarang digunakan. 
                Padahal, tutup botol bisa dijadikan seni mozaik yang mempercantik dekorasi rumah. 
                Cara membuatnya yakni :
            
            <ol class ="indent-8">
                <li>1. Kumpulkan tutup botol bekas sebanyak mungkin</li>
                <li>2. Siapkan kanvas atau karton, lem, dan cat</li>
                <li>3. Cari gambar yang ingin dibuat sebagai dekorasi</li>
                <li>4. Cat tutup botol sesuai gambar yang diinginkan</li>
                <li>5. Buat gambar di atas kanvas atau karton menggunakan pensil</li>
                <li>6. Letakkan tutup botol yang telah dicat di atas karton menggunakan lem </li>
                <li>7. Jika sudah selesai, gantungkan seni mozaik pada dinding</li>
            </ol>

            </p>

            <p class="subtext font-medium"> <br>4. Tikar dari kantong plastik</p>
            <p> Tikar merupakan anyaman yang biasanya dipakai sebagai alas duduk atau tidur. 
                Ternyata, kantong plastik bekas dapat dibuat menjadi tikar dengan langkah-langkah berikut :
            
            <ol class ="indent-8">
                <li>1. Kumpulkan kantong plastik bekas sebanyak mungkin dan pilah sesuai warnanya</li>
                <li>2. Siapkan gunjing, jarum, dan benang</li>
                <li>3. Pikirkan pola tikar yang akan dibuat</li>
                <li>4. Potong kantung pastik bekas menjadi strip panjang dengan lebar 5 cm</li>
                <li>5. Buat simpul dan kepanglah strip dengan warna yang sama</li>
                <li>6. Letakkan strip kantong plastik bekas yang telah dikepang dalam pola spiral </li>
                <li>7. Gunakan jarum dan benang untuk menjahit anyaman kantong plastik dalam bentuk spiral</li>
            </ol>

