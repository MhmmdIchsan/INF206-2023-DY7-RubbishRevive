            <p class="subtext font-medium"> <br>1. Pisahkan Sampah Anorganik </p><br>
            <p> Sampah rumahan terdiri dari dua jenis, yakni sampah organik dan sampah anorganik. Oleh karena itu, sebelum mulai mengolah sampah, sebaiknya pisahkan berdasarkan jenis sampah.

            <br>Cara paling tepat untuk memisahkan jenis sampah adalah dengan menyediakan setidaknya dua tempat sampah di rumah.
            <br>Satu tempat sampah dikhususkan untuk membuang sampah organik, sedangkan satu tempat sampah lagi untuk sampah anorganik.
            <br>Dengan melakukan langkah ini, ke depannya kamu tidak perlu repot-repot memisahkan sampah sekaligus mempermudah kerja tukang sampah.
            </p>

            <p class="subtext font-medium"> <br>2. Reduce </p><br>
            <p> Kampanye go green sudah ramai disuarakan sejak tahun-tahun ke belakang. Gerakan tersebut mendorong orang untuk mengurangi penggunaan benda-benda yang sulit terurai.

            <br>Pun seperti beberapa tren ke belakang, sebagaimana banyak diketahui penggunaan sedotan plastik dan tempat makan atau minum sekali pakai pun sudah mulai dikurangi.

            <br>Sebagai gantinya, kamu bisa menggunakan benda lain yang memiliki fungsi sama tapi tetap bisa digunakan berulang kali.

            <br>Misalnya menggunakan sedotan stainless steel, membawa tempat makan, atau membawa kantong belanja dari kain.

            <br>Dengan menerapkan hal seperti ini, kamu tidak perlu repot-repot berurusan dengan sampah yang sulit diurai.
            </p>

            <p class="subtext font-medium"> <br>3. Reuse </p><br>
            <p> Apakah kamu masih kesulitan menerapkan langkah reduce? Tenang, masih ada banyak cara untuk mengolah sampah-sampah seperti itu.
            <br>Salah satu cara yang bisa kamu coba dan penerapannya juga mudah adalah dengan langkah reuse atau menggunakannya kembali.
            <br>Cara ini juga bisa memberikan manfaat yang positif untuk kegiatan di rumah, misalnya menggunakan botol-botol kaca bekas sebagai tempat menyimpan bumbu dapur.
            <br>Atau selain itu, kamu juga bisa memanfaatkan kaleng-kaleng bekas untuk dijadikan pot tanaman.
            </p>

            <p class="subtext font-medium"> <br>4. Recycle </p><br>
            <p> Salah satu langkah mengolah sampah anorganik yang populer diterapkan adalah dengan cara recycle atau daur ulang.
            Cara pengolahan sampah ini memungkinkan untuk mengolah sampah menjadi benda baru yang memiliki nilai.<br>Salah satu cara yang bisa kamu coba dan penerapannya juga mudah adalah dengan langkah reuse atau menggunakannya kembali.
            <br>Langkah seperti ini tidak hanya bisa mengurangi sampah, tapi juga bisa menjadi ladang bisnis yang cukup menjanjikan.
            Contoh sederhananya adalah mengolah sampah kain menjadi taplak meja atau mengolah sampah plastik menjadi pot tanaman.
            <br>Namun jika kamu tidak memiliki waktu luang untuk mendaur ulang sampah, sebaiknya kamu menyerahkan sampah tersebut ke lembaga khusus daur ulang seperti website Rubbish Revive ini.
            </p>

