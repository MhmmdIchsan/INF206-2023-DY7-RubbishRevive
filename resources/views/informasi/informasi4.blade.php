        Sampah seperti itu bisa diolah dengan menerapkan 3R, yakni reduce (mengurangi), reuse (menggunakan kembali), dan recycle (mendaur ulang).        
        <br><br>Tanpa langkah pengolahan yang tepat, sampah-sampah seperti itu akan terus menumpuk dan menjadi salah satu penyumbang terbesar dalam kerusakan lingkungan.
