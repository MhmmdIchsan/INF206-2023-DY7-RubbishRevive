        <div class="text-justify"><p class=""> <br> Angka produksi kertas terus meningkat setiap tahunnya. 
        Dikutip dari Environmental Paper Network, produksi kertas global telah menembus angka 400 juta ton per tahun. 
        Setiap ton kertas yang diproduksi membutuhkan setidaknya 24 batang pohon. 
        Hal ini menunjukkan bahwa dalam setahun ada setidaknya 9.600 pohon yang ditebang untuk memenuhi kebutuhan kertas global. 
        <br><br>Semakin banyak kertas yang diproduksi, akan semakin banyak pula sampah kertas yang berakhir di Tempat Pembuangan Akhir. 
        Dikutip dari World Bank, setiap tahunnya manusia menghasilkan 2 miliar ton sampah. 
        Dimana sepertiga dari jumlah tersebut dibuang begitu saja tanpa adanya proses pemilahan dan 30% diantaranya adalah sampah kertas dan kardus. 
        <br><br>Timbulan sampah kertas yang cukup banyak di Tempat Pembuangan Akhir diakibatkan oleh kurangnya kesadaran masyarakat untuk melakukan pemilahan sampah. 
        Bahkan, beberapa orang lebih memilih untuk membakar sampah kertasnya daripada memilah dan menyalurkannya ke bank sampah karena dinilai lebih mudah untuk dilakukan. 
        Padahal membakar sampah kertas dapat menimbulkan pengaruh yang buruk bagi kesehatan manusia dan lingkungan</p>

            <p class="subtext font-medium"> <br>Apa yang Akan Terjadi Jika Sampah Kertas Dibakar? </p><br>
            <p> Membakar kertas mungkin menjadi pilihan banyak orang untuk memusnahkan dokumen yang berisi informasi penting. 
                Sifat kertas yang mudah terbakar membuat banyak orang berpikir membakarnya di tempat terbuka adalah cara yang paling efektif untuk mengurangi tumpukan kertas di rumah atau kantor. 
                Namun, terdapat risiko yang akan timbul  jika sampah kertas dibakar.
                <br><br> 
                Ketika diproduksi, kertas telah melewati serangkaian proses yang melibatkan bahan kimia. baik secara langsung dalam produksi kertas dan pulp atau dalam proses konversi (yaitu pencetakan, perekatan) yang mengikutinya. 
                Maka dari itu, saat dibakar, kertas akan melepaskan banyak bahan kimia beracun ke udara. Pembakaran sampah tidak hanya memberikan dampak buruk ke kondisi udara, tetapi juga bisa tersimpan di tanah,
                 terpapar ke permukaan air dan tanaman, hingga terhirup oleh manusia dan hewan. 

            </p>

            <p class="subtext font-medium"> <br>Daur Ulang: Cara Alternatif Kelola Sampah Kertas </p><br>
            <p> Walaupun terlihat efektif, membakar kertas banyak memberikan dampak buruk bagi lingkungan dan kehidupan manusia. Padahal terdapat alternatif lain untuk memusnahkan dokumen berbasis kertas, yaitu dengan daur ulang. 

            <br><br>Mendaur ulang barang berbasis kertas dapat menghemat banyak ruang Tempat Pembuangan Akhir sekaligus juga mengurangi timbulan sampah kertas bercampur dengan sampah lainnya. 
            Risiko dari pencemaran lingkungan pun dapat diminimalisir dengan melakukan daur ulang.

            <br><br>Kertas merupakan salah satu bahan yang paling mudah untuk didaur ulang dan dapat digunakan kembali. 
            Dengan memilah dan mendaur ulang kertas, kita bisa menghemat energi dan air hingga 70% dibandingkan dengan membuat kertas baru dari pohon. Kertas juga dapat didaur ulang sebanyak 4-6 kali sebelum akhirnya serat selulosanya benar-benar tidak bisa didaur ulang kembali.
            
            <br><br>Dengan menambahkan unsur kreativitas, Anda dapat menghembuskan kehidupan baru ke dalam kertas bekas. 
            Terdapat beberapa cara mudah untuk menjadi kreatif dengan memanfaatkan kertas bekas, yaitu sebagai berikut: 
            <ul>
                <li>Banyak kertas cetak hanya memanfaatkan satu sisi. Anda dapat memotongnya menjadi potongan-potongan kecil dan menggunakan sisi yang tidak digunakan untuk catatan Anda.</li>
                <li>Jangan membuang koran bekas. Anda dapat menyimpannya untuk membungkus hadiah.</li>
                <li>Ubah kertas lama dan bekas menjadi dekorasi untuk acara ulang tahun ataupun acara formal.</li>
                <li>Gunakan kembali karton untuk menyimpan inventaris.</li>
            </ul>

            <br>
            Banyak cara yang dapat dilakukan untuk memusnahkan kertas selain membakarnya.
